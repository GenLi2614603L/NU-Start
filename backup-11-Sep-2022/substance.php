    	                                    	Nu Start Community Enterprise delivers high quality interventions for young people and adults on substance misuse issues.<br /><br />

We have qualified psychologist, psychotherapist and substance misuse practitioners who are in place to support and help individuals with difficulties and struggles with drug and alcohol abuse. <br /><br />

We work in partnership with a number of organisations and services and use preventative strategies to support young people from entering into a drug using career. <br /><br />

The way we do this is to provide information and knowledge of substance misuse by holding workshops and courses that promote awareness on the subject matter. 
<br /><br />
We follow this up by supporting young people into services and placements where they can harness their new found skills and knowledge in an environment where it can nourish and blossom, thus decreasing the likelihood of them entering into a substance misusing career. <br /><br />

We identify individual's urges, cravings and triggers and look at high risk situations which can be antecedents for drug misuse. <br /><br />

We have a clear model in that we screen and assess individuals to identify the level of need and support that they require, and from this a care plan is put in place to work with the individual towards an agreed goal(s). <br /><br />

We have a number of interventions that our staff use to support individuals and we predominately work within a Psycho-Social model. 
