                                          <p><strong>Film Production Course</strong>(6 Month)</p>
                                          <p class="text-2">Course Overview</p>
                                          
                                          <p class="text-2">Our new Six-Month Practical Filmmaking programme has been created for people looking for an intensive and practical immersion in the craft and technique of filmmaking. The programme is specifically designed to give you the essential tools to be a professional filmmaker and a solid foundation in the key elements of filmmaking, from script to screen. Taught by industry professionals, Six-Month Practical Filmmaking will provide you with the confidence to take your skills into the film industry, as well as a showreel to launch your career.</p>
 
<p class="text-2">The 25 week programme consists of three terms. During Terms 1 and 2, you will be trained in all key aspects of filmmaking through a combination of intensive practical workshops and a series of filmmaking exercises, escalating in scale as you go through the programme. Following the learning from the foundation programme in Term 1, you will learn advanced skills in screenwriting; cinematography and sound; fiction directing and producing; documentary production; editing and postproduction during Term 2. In Term 3, you will make your own film and perform key roles on set on other students' films.
</p>
<p class="text-2">Students who demonstrate exemplary communication and teamwork, management, planning and organisation skills, initiative and film sense are awarded a Practice Certificate, and access to our Industry Placement Scheme.<br /><br />
 
For more information Request A Prospectus and our Enrolments Team will get in touch to talk you through the course</p><br /><br />

